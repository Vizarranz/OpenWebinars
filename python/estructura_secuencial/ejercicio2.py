# Calcular el perímetro y área de un rectángulo dada su base y altura.

base = float(input("Introduzca la medida de la base: "));
height = float(input("Introduzca la medida de la base: "));

perimeter = 2 * base * height;
area = base * height;

print("El perímetro es:" ,perimeter, "y el área del triángulo es:", area);